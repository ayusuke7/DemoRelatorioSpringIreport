package com.example.demo;

public class ImageSource {
    private String url;
    private String data;

    public ImageSource(String url, String data){
        this.url = url;
        this.data = data;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }
}
